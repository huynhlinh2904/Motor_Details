package com.example.listviewdemo.schemes;

public class Link {
    private Href self;
    private Href edit;
    private Href avatar;

    public Href getSelf() {
        return self;
    }

    public void setSelf(Href self) {
        this.self = self;
    }

    public Href getEdit() {
        return edit;
    }

    public void setEdit(Href edit) {
        this.edit = edit;
    }

    public Href getAvatar() {
        return avatar;
    }

    public void setAvatar(Href avatar) {
        this.avatar = avatar;
    }
}
